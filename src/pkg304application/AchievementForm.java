    private boolean validInput() {
        try{
            int num = Integer.parseInt(totalNeeded.getText());
            if (achievementName.getText().equals("")){
                return false;
            }    
            if (num < 0){
                return false;
            }
        } catch (NumberFormatException nfe){
            return false;
        }
        return true;
    }
